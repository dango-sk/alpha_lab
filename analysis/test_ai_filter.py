def print_result(date: str, result: dict):
    portfolio = result.get("final_portfolio", [])
    print(f"\n{'='*65}")
    print(f"  [{date}] 최종 {len(portfolio)}종목")
    print(f"  {'종목명':<18}  {'비중':>6}  {'신뢰도':>6}")
    print(f"  {'-'*40}")
    for s in portfolio:
        name = s.get("stock_name", s.get("stock_code", ""))
        wt   = s.get("weight_pct", 0)
        conf = s.get("confidence", "")
        print(f"  {name:<18}  {wt:>5.1f}%  {conf}")
    print(f"  {'='*40}")
    summary = result.get("consensus_result", {}).get("decision_summary", "")
    if summary:
        print(f"  요약: {summary[:80]}...")


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--date", default=None, help="특정 날짜만 실행 (예: 2026-01-02)")
    args = parser.parse_args()

    dates = [args.date] if args.date else list(MONTHLY_STOCKS.keys())
    out_dir = Path(__file__).parent / "results"
    out_dir.mkdir(exist_ok=True)

    conn = get_conn()
    try:
        for date in dates:
            stocks = MONTHLY_STOCKS.get(date)
            if not stocks:
                print(f"날짜 없음: {date}")
                continue

            print(f"\n{'='*65}")
            print(f"  {date} 실행 중... (30종목 → AI 필터링)")
            print(f"{'='*65}")

            result = run_ai_filter(stocks, date, conn)

            out_path = out_dir / f"ai_filter_{date}.json"
            with open(out_path, "w", encoding="utf-8") as f:
                json.dump(result, f, ensure_ascii=False, indent=2, default=str)

            print_result(date, result)
            print(f"\n  [저장] {out_path}")

    finally:
        conn.close()

    print("\n\n전체 완료!")


if __name__ == "__main__":
    main()
